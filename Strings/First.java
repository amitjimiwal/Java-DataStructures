package Strings;
/*
 * String is a non Primitive data type/ reference data type
 * It's basically a collection of character.
 * Immutable
 */
public class First {
     public static void main(String[] args) {
          //Types of declaring String
          String s="Amit";
          String a=new String("Amit");

          //pretty formaating in the strings
          System.out.printf("Hello my name is %s and %s",s,a);
          System.out.println();

          //iteration
          for (int i = 0; i < s.length(); i++) {
               System.out.println(s.charAt(i));
          }
     }
}
